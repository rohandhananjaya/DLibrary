    public void sDt(int index){
    
        try{
            Connection con  = DB.createConnection();
            
            Statement stmt = con.createStatement();
                
            ResultSet rs = stmt.executeQuery("SELECT * FROM studata WHERE id='"+index+"'");
    
           //id,inm,nm,nic,adr,mail,gen,con,idt,rdt
            tEnbl(false);
            
            while(rs.next())
             {
                lblid.setText(rs.getString("id"));
                txtind.setText(rs.getString("inm"));
                txtnm.setText(rs.getString("nm"));
                txtnic.setText(rs.getString("nic"));
                txtad.setText(rs.getString("adr"));
                txtcn.setText(rs.getString("con"));
                txtml.setText(rs.getString("mail"));
                
                gen=rs.getString("gen");
                
                if(gen.equals("Male")){
                    gml.setSelected(true);
                    gfl.setEnabled(false);
                }
                else{
                    gfl.setSelected(true);
                    gml.setEnabled(false);
                }
             }  
        }
        catch(Exception e)
        {
            System.out.println(e.getMessage());
        }
    }
