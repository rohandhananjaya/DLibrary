    private void snw (){
                try{

                    
            if(gml.isSelected()){
                gen="Male";
            }
            else if(gfl.isSelected()){
                gen="Female";
            }

            if(txtnm.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Name Cannot be Empty");
            }
                
            Connection con  = DB.createConnection();
            
            Statement stmt = con.createStatement();
                
            ResultSet rs = stmt.executeQuery("SELECT COUNT(*) FROM studata");
     
            rs.next();
            
            //int rowCount = rs.getInt(1);
            
            String SQL="INSERT INTO studata (id,inm,nm,nic,adr,mail,gen,con) VALUES(?,?,?,?,?,?,?,?)";
            
            PreparedStatement pstmt  = con.prepareStatement(SQL);

            pstmt.setString(1, lblid.getText());
            pstmt.setString(2, txtind.getText());
            pstmt.setString(3, txtnm.getText());
            pstmt.setString(4, txtnic.getText());
            pstmt.setString(5, txtad.getText());
            pstmt.setString(6, txtml.getText());
            pstmt.setString(7, gen);
            pstmt.setString(8, txtcn.getText());

            pstmt.executeUpdate();
            pstmt.close();

           JOptionPane.showMessageDialog(this,"Data sucsesfuly submited","Submit",JOptionPane.INFORMATION_MESSAGE);

           lblid.setText("");
           txtind.setText("");
           txtnm.setText("");
           txtnic.setText("");
           txtad.setText("");
           txtcn.setText("");
           txtml.setText("");
           
           lblid.setText(String.valueOf(c.cMem()+1));

        }

        catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
    
        private void upd (){
            
            try{
                    
            if(gml.isSelected()){
                gen="Male";
            }
            else if(gfl.isSelected()){
                gen="Female";
            }

            if(txtnm.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Name Cannot be Empty");
            }
            
            int nid= Integer.parseInt(lblid.getText());
            
            Connection con  = DB.createConnection();
            
            Statement stmt = con.createStatement();
            //studata ,inm,nm,nic,adr,mail,gen,con,
            
            stmt.executeUpdate("update studata set inm='"+txtind.getText()+"' where id='"+nid+"'");
            stmt.executeUpdate("update studata set nm='"+txtnm.getText()+"' where id='"+nid+"'");
            stmt.executeUpdate("update studata set nic='"+txtnic.getText()+"' where id='"+nid+"'");
            stmt.executeUpdate("update studata set adr='"+txtad.getText()+"' where id='"+nid+"'");
            stmt.executeUpdate("update studata set mail='"+txtml.getText()+"' where id='"+nid+"'");
            stmt.executeUpdate("update studata set gen='"+gen+"' where id='"+nid+"'");
            stmt.executeUpdate("update studata set con='"+txtcn.getText()+"' where id='"+nid+"'");    
            
            JOptionPane.showMessageDialog(this,"Data sucsesfuly updated","Edit",JOptionPane.INFORMATION_MESSAGE);
                
                tEnbl(false);
                btnSub.setEnabled(false);
                btnedit.setEnabled(true);
                btnDel.setEnabled(true);

                if(gen.equals("Male")){
                    gml.setSelected(true);
                    gfl.setEnabled(false);
                }
                else{
                    gfl.setSelected(true);
                    gml.setEnabled(false);
                }

        }

        catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
    
