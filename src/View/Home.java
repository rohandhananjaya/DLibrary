    public void udTbl(){
        DefaultTableModel model = (DefaultTableModel)Tbl1.getModel();

        while(model.getRowCount() > 0){
            for(int i = 0 ; i < model.getRowCount();i++){
                model.removeRow(i);
                
         }
        }
        lblst.setText("");
    }
    private void schBook(){
        
              String st=  txtschbr.getText();
            try{
                  
                Connection con = DB.createConnection();
                
                Statement stmt = con.createStatement();
                
                ResultSet rs=null ;
                
                //String ctext=comfl.getSelectedItem().toString();
                int x=comfl.getSelectedIndex();
                
                if(st.equals("*")){
                    rs = stmt.executeQuery("SELECT * FROM book_data");
                }
                else{
                    if(x==0){
                        rs = stmt.executeQuery("SELECT * FROM book_data WHERE id='"+st+"'");
                    }
                    else if(x==1){
                         rs = stmt.executeQuery("SELECT * FROM book_data WHERE name='"+st+"'");
                    }
                    else if(x==2){
                        rs = stmt.executeQuery("SELECT * FROM book_data WHERE auth='"+st+"'");
                    }
                    else if(x==3){
                         rs = stmt.executeQuery("SELECT * FROM book_data WHERE cat='"+st+"'");
                    }   
                    else if(x==4){
                        rs = stmt.executeQuery("SELECT * FROM book_data WHERE isbn='"+st+"'");
                    }
                    else if(x==5){
                        rs = stmt.executeQuery("SELECT * FROM book_data WHERE avbl='"+st+"'");
                    }
                else{
                     rs = stmt.executeQuery("SELECT * FROM book_data");
                    }
                }
                
                
             while(rs.next())
             {
                 String bid=rs.getString("id");
                 String nm=rs.getString("name");
                 String auth=rs.getString("auth");
                 String cat=rs.getString("cat");
                 String isbn=rs.getString("isbn");
                 String avb=rs.getString("avbl");
                 
               DefaultTableModel model = (DefaultTableModel) Tbl1.getModel();
               Object[] row = {bid, nm, auth,cat ,isbn,avb};
                model.setRowCount(model.getRowCount());
                model.addRow(row);
                this.Tbl1.setModel(model);
             }  
                 
              }
              catch(Exception e){
                  
              }
    }
