    private void Tbl1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Tbl1MouseClicked
    
      int mc=evt.getClickCount();
     
      if (mc>1){
       
          int n= Tbl1.getSelectedRow();
      
            DefaultTableModel model = (DefaultTableModel)Tbl1.getModel();
            String md= model.getValueAt(n, 0).toString();//model.getValueAt(n, 0).toString();

            if (opbk.isSelected()){
                //System.out.println(md);
               Regbook rb= new Regbook(this,true,true);
               rb.sDt(Integer.parseInt(md));
               rb.setTitle("Book ID : "+md);
                rb.setVisible(true);
            }
            else{
                RegisterStudent rs= new RegisterStudent(this,true,true);
                rs.sDt(Integer.parseInt(md));
                rs.setTitle("Student ID : "+md);
                rs.setVisible(true);
            }
      }

    }//GEN-LAST:event_Tbl1MouseClicked

